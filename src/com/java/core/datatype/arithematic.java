public class arithematic {
	public static void main(String[] args) {
		
		int a = 12;
		int b = 54;
		int c = 65;
		int d = 21;
		
		System.out.println(a+b);
		System.out.println(a-b);
		System.out.println(a*b);
		System.out.println(a/b);
		System.out.println(a+b+c+c);
		System.out.println(a*c*d);
		System.out.println((a+d) + (c+b));
		System.out.println((a*b) - (c*d));
	}

}
